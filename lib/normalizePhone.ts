/**
 * Приводить український номер телефону до канонічного вигляду +380XXXXXXXXX.
 *
 * Прибирає всі нецифрові символи (пробіли, дужки, дефіси, провідний «+»)
 * і нормалізує локальні написання до єдиної форми:
 *
 *   "050 123 45 67"      → "+380501234567"
 *   "(050) 123-45-67"    → "+380501234567"
 *   "0501234567"         → "+380501234567"   // 0XXXXXXXXX
 *   "380501234567"       → "+380501234567"   // без «+»
 *   "+380 50 123 45 67"  → "+380501234567"
 *
 * Якщо рядок не схожий на UA-номер — повертає "+" + цифри, тобто свідомо
 * невалідне значення, яке відхилить валідація вище по стеку.
 *
 * Спільна утиліта: використовується І в zod-трансформі реєстрації
 * (schemas/register.ts), І в POST /api/register. Завдяки цьому різні
 * написання того самого номера не пройдуть як різні записи.
 */
export function normalizePhone(input: string): string {
  const digits = input.replace(/\D/g, "");

  // 0XXXXXXXXX (локальний запис, 10 цифр) → +380XXXXXXXXX
  if (digits.length === 10 && digits.startsWith("0")) {
    return `+38${digits}`;
  }

  // 380XXXXXXXXX (12 цифр, без «+») → +380XXXXXXXXX
  if (digits.length === 12 && digits.startsWith("380")) {
    return `+${digits}`;
  }

  // XXXXXXXXX (9 цифр, без коду країни та оператора-нуля) → +380XXXXXXXXX
  if (digits.length === 9) {
    return `+380${digits}`;
  }

  // Невідомий формат — повертаємо як є, валідація його відхилить.
  return `+${digits}`;
}
